class DisneyChar {
    constructor(_id, films, shortFilms, tvShows, videoGames, parkAttractions, 
                allies, enemies, name, imageUrl, url) {
      this._id = _id;
      this.films = films;
      this.shortFilms = shortFilms;
      this.tvShows = tvShows;
      this.videoGames = videoGames;
      this.parkAttractions = parkAttractions;
      this.allies = allies;
      this.enemies = enemies;
      this.name = name;
      this.imageUrl = imageUrl;
      this.url = url;
    }
  }
  
  export default DisneyChar;